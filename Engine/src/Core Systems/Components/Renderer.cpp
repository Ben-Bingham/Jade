	void nullRender(GameObject& gameObject) {
		std::string message = "The game object with ID: ";
		message += std::to_string(gameObject.getID());
		message += " does not have a render function asociated with it, and probably should.";
		LOGGER.log(message, Jade::WARNING);
	}

	void cubeRender(GameObject& gameObject) {
		//Bind VAO and draw
		gameObject.renderer->getVAO();
		glDrawArrays(GL_TRIANGLES, 0, 36);
	}

	Renderer::Renderer(GameObject& gameObject, std::vector<float> verticies) 
		: m_GameObject(gameObject), m_Verticies(verticies), m_VAO(), m_VBO(verticies) {

		gameObject.setRenderFunction(nullRender);

		VertexAttributePointer positionData(3, GL_FLOAT);
		VertexAttributePointer texCordData(2, GL_FLOAT);

		std::vector<VertexAttributePointer> attributePointers = {
			positionData,
			texCordData
		};

		m_VAO.addAttributePointers(attributePointers);
	}
